	GISDETAILS BIGINT NOT NULL, -- GISDETAILS ID
	APPL_ZONE BIGINT, -- APPLICATION ZONE ID
	APPLICATION_TAX double precision,
	CREATEDBY BIGINT NOT NULL,
	LASTMODIFIEDBY BIGINT NOT NULL,
	CREATEDDATE TIMESTAMP WITHOUT TIME ZONE NOT NULL,
	LASTMODIFIEDDATE TIMESTAMP WITHOUT TIME ZONE NOT NULL,
	VERSION BIGINT,
	CONSTRAINT PK_EGPT_GIS_AUD_DET_ID PRIMARY KEY (ID),
	CONSTRAINT FK_EGPT_GIS_AUD_DET_CREATEDBY FOREIGN KEY (CREATEDBY) REFERENCES EG_USER (ID),
	CONSTRAINT FK_EGPT_GIS_AUD_DET_LASTMODBY FOREIGN KEY (LASTMODIFIEDBY) REFERENCES EG_USER (ID),
	CONSTRAINT FK_EGPT_GIS_AUD_DET_GISDET FOREIGN KEY (GISDETAILS) REFERENCES EGPT_GIS_DETAILS (ID)
);

