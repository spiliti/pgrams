package org.egov.ptis.domain.entity.property;

import org.egov.infra.persistence.entity.AbstractAuditable;
import org.egov.infra.persistence.validator.annotation.Unique;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;

@Entity
@Table(name = "EGPT_OWNERAUDIT")
@Unique(id = "id", tableName = "EGPT_OWNERAUDIT")

@SequenceGenerator(name = OwnerAudit.SEQ_OWNERAUDIT, sequenceName = OwnerAudit.SEQ_OWNERAUDIT, allocationSize = 1)
public class OwnerAudit extends AbstractAuditable {

    private static final long serialVersionUID = 1L;
    public static final String SEQ_OWNERAUDIT = "SEQ_EGPT_OWNERAUDIT";

    @Id
    @GeneratedValue(generator = SEQ_OWNERAUDIT, strategy = GenerationType.SEQUENCE)
    private Long id;

    @NotNull
    @Column(name = "id_basic_property")
    private Long basicproperty;

    @Column(name = "id_user")
    private Long userId;

    private String doorNo;

    private String aadhaarNo;

    private String mobileNo;

    private String ownerName;

    private String gender;

    private String emailId;

    private String guardianRelation;

    private String guardianName;

    public String getDoorNo() {
        return doorNo;
    }

    public void setDoorNo(String doorNo) {
        this.doorNo = doorNo;
    }

    public String getAadhaarNo() {
        return aadhaarNo;
    }

    public void setAadhaarNo(String aadhaarNo) {
        this.aadhaarNo = aadhaarNo;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public void setMobileNo(String mobileNo) {
        this.mobileNo = mobileNo;
    }

    public String getOwnerName() {
        return ownerName;
    }

    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    public String getGuardianRelation() {
        return guardianRelation;
    }

    public void setGuardianRelation(String guardianRelation) {
        this.guardianRelation = guardianRelation;
    }

    public String getGuardianName() {
        return guardianName;
    }

    public void setGuardianName(String guardianName) {
        this.guardianName = guardianName;
    }

    public Long getBasicproperty() {
        return basicproperty;
    }

    public void setBasicproperty(Long basicproperty) {
        this.basicproperty = basicproperty;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(final Long id) {
        this.id = id;
    }

}
