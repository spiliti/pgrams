package org.egov.ptis.domain.entity.document;

import org.egov.infstr.models.BaseModel;

import java.util.Date;

public class DocumentTypeDetails extends BaseModel {

    /**
     * 
     */
    private static final long serialVersionUID = 2485256615455834019L;
    private Long basicPropertyId;
    private String documentNo;
    private Date documentDate;
    private String proceedingNo;
    private Date proceedingDate;
    private String courtName;
    private String documentName;
    private boolean signed;

    public Long getBasicPropertyId() {
        return basicPropertyId;
    }

    public void setBasicPropertyId(Long basicPropertyId) {
        this.basicPropertyId = basicPropertyId;
    }

    public String getDocumentNo() {
        return documentNo;
    }

    public void setDocumentNo(String documentNo) {
        this.documentNo = documentNo;
    }

    public Date getDocumentDate() {
        return documentDate;
    }

    public void setDocumentDate(Date documentDate) {
        this.documentDate = documentDate;
    }

    public String getProceedingNo() {
        return proceedingNo;
    }

    public void setProceedingNo(String proceedingNo) {
        this.proceedingNo = proceedingNo;
    }

    public Date getProceedingDate() {
        return proceedingDate;
    }

    public void setProceedingDate(Date proceedingDate) {
        this.proceedingDate = proceedingDate;
    }

    public String getCourtName() {
        return courtName;
    }

    public void setCourtName(String courtName) {
        this.courtName = courtName;
    }

    public String getDocumentName() {
        return documentName;
    }

    public void setDocumentName(String documentName) {
        this.documentName = documentName;
    }

    public boolean isSigned() {
        return signed;
    }

    public void setSigned(boolean signed) {
        this.signed = signed;
    }

}
