package org.egov.restapi.web.contracts.applications;

import java.util.Date;

public class ApplicationSearchResponse {

	private String moduleName;
    private String applicationNumber;
    private Date applicationDate;
    private String applicationType;
    private String applicantName;
    private String applicantAddress;
    private Date disposalDate;
    private String status;
    private String consumerCode;
    private String mobileNumber;
    private String ownerName;
    private String aadharNumber;
    private String approvalStatus;
    private String channel;
    private String cityCode;
    private String cityName;
	
    public ApplicationSearchResponse(String moduleName, String applicationNumber, Date applicationDate,
			String applicationType, String applicantName, String applicantAddress, Date disposalDate, String status,
			String consumerCode, String mobileNumber, String ownerName, String aadharNumber,
			String approvalStatus, String channel, String cityCode, String cityName) {
		this.moduleName = moduleName;
		this.applicationNumber = applicationNumber;
		this.applicationDate = applicationDate;
		this.applicationType = applicationType;
		this.applicantName = applicantName;
		this.applicantAddress = applicantAddress;
		this.disposalDate = disposalDate;
		this.status = status;
		this.consumerCode = consumerCode;
		this.mobileNumber = mobileNumber;
		this.ownerName = ownerName;
		this.aadharNumber = aadharNumber;
		this.approvalStatus = approvalStatus;
		this.channel = channel;
		this.cityCode = cityCode;
		this.cityName = cityName;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getApplicationNumber() {
		return applicationNumber;
	}

	public void setApplicationNumber(String applicationNumber) {
		this.applicationNumber = applicationNumber;
	}

	public Date getApplicationDate() {
		return applicationDate;
	}

	public void setApplicationDate(Date applicationDate) {
		this.applicationDate = applicationDate;
	}

	public String getApplicationType() {
		return applicationType;
	}

	public void setApplicationType(String applicationType) {
		this.applicationType = applicationType;
	}

	public String getApplicantName() {
		return applicantName;
	}

	public void setApplicantName(String applicantName) {
		this.applicantName = applicantName;
	}

	public String getApplicantAddress() {
		return applicantAddress;
	}

	public void setApplicantAddress(String applicantAddress) {
		this.applicantAddress = applicantAddress;
	}

	public Date getDisposalDate() {
		return disposalDate;
	}

	public void setDisposalDate(Date disposalDate) {
		this.disposalDate = disposalDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getConsumerCode() {
		return consumerCode;
	}

	public void setConsumerCode(String consumerCode) {
		this.consumerCode = consumerCode;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getOwnerName() {
		return ownerName;
	}

	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	public String getAadharNumber() {
		return aadharNumber;
	}

	public void setAadharNumber(String aadharNumber) {
		this.aadharNumber = aadharNumber;
	}

	public String getApprovalStatus() {
		return approvalStatus;
	}

	public void setApprovalStatus(String approvalStatus) {
		this.approvalStatus = approvalStatus;
	}

	public String getChannel() {
		return channel;
	}

	public void setChannel(String channel) {
		this.channel = channel;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
    
}
